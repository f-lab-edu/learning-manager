package me.chan99k.learningmanager.course;

/**
 * [P2] 스터디 과정 삭제
 * <p>
 * - **주요 액터(Actor):** 사용자(스터디장)
 * <p>
 * - **사전 조건:** 스터디장은 로그인되어 있으며, 자신이 관리하는 스터디 과정이 존재한다.
 * <p>
 * - **성공 시나리오:**
 *     1. 스터디장이 과정 삭제를 요청한다.
 *     2. 시스템은 해당 과정과 연관된 모든 데이터(멤버, 커리큘럼)를 삭제한다.
 * <p>
 * - **실패 시나리오:**
 *     - **(권한 없음):** 해당 과정의 스터디장이 아닌 사용자가 삭제를 시도할 경우, "권한이 없습니다."라는 메시지를 반환한다.
 *     - **(과정 없음):** 존재하지 않는 과정을 삭제하려고 할 경우, "권한이 없습니다."라는 메시지를 반환한다.
 * <p>
 * - **연관 도메인:** 없음
 */
public interface CourseDeletion {
	void deleteCourse(Long courseId);
}
