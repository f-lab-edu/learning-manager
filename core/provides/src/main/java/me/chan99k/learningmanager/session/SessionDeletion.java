package me.chan99k.learningmanager.session;

/**
 * [P2] 스터디 세션 삭제
 * <p>
 * - 주요 액터(Actor): 사용자(스터디장), 시스템 관리자
 * <p>
 * - 사전 조건:
 *     - (스터디장): 로그인되어 있으며, 삭제할 세션의 과정 관리 권한이 있다.
 *     - (시스템 관리자): 단독 세션의 경우 시스템 관리자 권한이 있다.
 *     - 삭제할 세션이 존재해야 한다.
 * <p>
 * - 성공 시나리오:
 *     1. 액터가 특정 세션의 삭제를 요청한다.
 *     2. 시스템은 삭제 권한을 확인한다.
 *     3. 시스템은 세션을 삭제한다.
 * <p>
 * - 실패 시나리오:
 *     - (권한 없음): 해당 세션을 삭제할 권한이 없는 사용자가 시도할 경우, "권한이 없습니다."라는 메시지를 반환한다.
 *     - (세션 미존재): 존재하지 않는 세션을 삭제하려 할 경우, "세션을 찾을 수 없습니다."라는 메시지를 반환한다.
 *     - (삭제 불가): 하위 세션이 존재하는 세션의 경우, "삭제할 수 없는 세션입니다."라는 메시지를 반환한다.
 * <p>
 * - 연관 도메인: Course
 */
public interface SessionDeletion {
	void deleteSession(Long sessionId);
}
